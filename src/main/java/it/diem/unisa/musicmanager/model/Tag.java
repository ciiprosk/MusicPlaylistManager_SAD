package it.diem.unisa.musicmanager.model;

/**
 * Rappresenta i tag applicabili a una traccia musicale per classificazione o etichettatura.
 */
public enum Tag {
    /** Indica che la traccia è tra i preferiti dell'utente. */
    FAVOURITE,
    /** Indica che la traccia contiene contenuti espliciti. */
    EXPLICIT,
    /** Indica che la traccia è una nuova uscita. */
    NEWRELEASE
}
